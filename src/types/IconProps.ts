export interface IconProps {
  /**
   * Height, in pixels.
   * @defaultValue 24
   */
  height?: number;
  /**
   * Height and width, in pixels. Takes priority over {@link height} and
   * {@link width} properties.
   */
  size?: number;
  /**
   * Path stroke width.
   * @defaultValue 2
   */
  strokeWidth?: number;
  /**
   * Width, in pixels.
   * @defaultValue 24
   */
  width?: number;
}
